
import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef {
    public static void main(String[] args) throws java.lang.Exception {

        Scanner sc = new Scanner(System.in);
        int test = sc.nextInt();
        while (test != 0) {

            int n= sc.nextInt();
            for(int i=n;i>0;i--){
                System.out.print(i+" ");
            }
            test--;
        }
    }



}




